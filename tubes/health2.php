            <h5 class="card-title"><b>10 Minuman Penurun Berat Badan yang Ampuh Buat Diet</b></h5>
            <p class="card-text"><small class="text-body-secondary">Last updated Selasa, 16 Mei 2023 08:36 WIB</small></p>
            <p class="card-text">Bukan cuma makanan yang bisa menurunkan berat badan. Ada juga minuman penurun berat badan yang bisa dinikmati untuk diet.</p>
            <p>Meskipun tidak ada minuman penurun berat badan yang ajaib, minuman tertentu membuat Anda kenyang lebih lama, yang dapat mencegah Anda mengonsumsi kalori berlebih sepanjang hari.</p>
            <p><b>Berikut beberapa minuman penurun berat badan:</b></p>
            <p><b>1. Air putih</b></p>
            <p>Jika Anda merasa lapar, minumlah air terlebih dahulu karena rasa lapar adalah tanda dehidrasi. Air adalah salah satu pilihan terbaik Anda untuk menurunkan berat badan. Tidak hanya bebas kalori, tetapi juga membuat Anda kenyang sehingga Anda tidak makan berlebihan, kata Kristin Gillespie, seorang dokte nutrisi bersertifikat di Option Care Health dikutip dari Insider.</p>
            <p>Faktanya, penelitian tahun 2019 menemukan bahwa peningkatan asupan air dikaitkan dengan penurunan berat badan rata-rata 5,15 persen pada orang yang minum banyak air.</p>
            <p>Reisdorf menyarankan untuk minum air putih setidaknya setengah berat badan Anda setiap hari (dalam ons bukan liter).</p>
            <p><b>2. Teh hijau</b></p>
            <p>Teh hijau mengandung katekin dan kafein - dua senyawa yang dapat meningkatkan metabolisme, kata Gillespie. Minuman penurun berat badan ini juga mengandung kafein, yang menurut penelitian dapat membantu membakar lemak dan meningkatkan kalori yang Anda bakar saat istirahat.</p>
            <p>Faktanya, sebuah studi kecil tahun 2005 tentang subjek yang kelebihan berat badan dan obesitas sedang menemukan bahwa mengonsumsi campuran teh hijau dengan 150 miligram kafein selama tiga bulan dikaitkan dengan penurunan berat badan dan penurunan lemak yang lebih banyak, serta pengurangan lingkar pinggang yang lebih signifikan. dibandingkan dengan mereka yang minum plasebo.</p>
            <p><b>3. Teh hitam</b></p>
            <p>Seperti teh hijau, teh hitam penuh dengan antioksidan, khususnya polifenol, yang mengurangi risiko obesitas. Misalnya, tinjauan tahun 2018 menemukan bahwa polifenol dalam teh mengurangi seberapa banyak tubuh Anda menyerap lemak dan gula, sehingga mengurangi asupan kalori dan mendukung penurunan berat badan.</p>
            <p>Selain itu, sebuah studi kecil tahun 2014 menemukan bahwa minum tiga cangkir teh hitam sehari selama tiga bulan mengurangi risiko kenaikan berat badan peserta dan mengurangi lingkar pinggang mereka sekitar dua sentimeter dibandingkan dengan kelompok plasebo yang mengonsumsi minuman dengan rasa dan kafein yang sama. konten, tapi tanpa polifenol.</p>
            <p><b>4. Kefir</b></p>
            <p>Kefir adalah minuman susu fermentasi yang mengandung hingga 61 jenis probiotik yang menyehatkan usus.</p>
            <p>"Probiotik telah terbukti mengurangi berat badan dan lemak perut karena perannya dalam resistensi insulin, peradangan, dan penyimpanan lemak," kata Erin Kenney, CEO Nutrition Rewired.</p>
            <p>Faktanya, sebuah studi kecil tahun 2016 menemukan bahwa wanita yang kelebihan berat badan dan obesitas dengan diet terbatas kaya susu dan non-kalori yang minum dua porsi minuman kefir setiap hari selama delapan minggu mengalami penurunan berat badan, BMI, dan lingkar pinggang yang jauh lebih besar daripada mereka yang tidak mengkonsumsinya.</p>
            <img src="img/jus2.jpeg" class="card-img-top" alt="da">
            <p><b>5. Protein shake</b></p>
            <p> Bubuk protein seperti protein whey, protein kedelai, protein putih telur, protein kasein, dan protein kacang semuanya terbukti membantu menurunkan berat badan. Itu karena protein dalam protein shake membuat Anda merasa kenyang lebih lama dan juga membutuhkan lebih banyak energi untuk dicerna tubuh Anda, sehingga Anda membakar lebih banyak kalori.</p>
            <p>Faktanya, tinjauan tahun 2014 menemukan bahwa mencerna protein dapat meningkatkan jumlah kalori yang dibakar tubuh Anda saat istirahat sebesar 15 persen hingga 30 persen sementara karbohidrat dan lemak hanya menyebabkan peningkatan 3 persen hingga 10 persen.</p>
            <p> Itu bisa membantu menjelaskan mengapa diet tinggi protein membakar lebih banyak kalori berjam-jam setelah makan dibandingkan dengan diet lainnya. Reisdorf merekomendasikan untuk memakai 20 gram protein per shake.</p>
            <p><b>6. Kopi Hitam</b></p>
            <p>Jika Anda tidak menyukai kopi hitam, pertimbangkan untuk menambahkan alternatif susu seperti susu almond yang lebih rendah kalori dan lemak jenuhnya daripada susu tradisional.</p>
            <p><b>7. Cuka sari apel</b></p>
            <p>Asam asetat, bahan aktif dalam cuka sari apel, meningkatkan metabolisme dan menekan nafsu makan, menurut Gillespie. Selain itu, ini dapat memperlambat laju pengosongan makanan dari perut, sehingga membuat Anda merasa kenyang lebih lama.
            </p>
            <p>Faktanya, satu studi kecil tahun 2009 pada orang dewasa Jepang yang gemuk menemukan bahwa mengonsumsi 2 sendok makan cuka yang dicampur dengan minuman setiap hari menyebabkan penurunan lemak tubuh 0,9 persen.
            </p>
            <p>Faktanya, satu studi kecil tahun 2009 pada orang dewasa Jepang yang gemuk menemukan bahwa mengonsumsi 2 sendok makan cuka yang dicampur dengan minuman setiap hari menyebabkan penurunan lemak tubuh 0,9 persen.
            </p>
            <p><b>8. Jus Sayuran</b></p>
            <p>Meskipun makan sayuran selalu lebih disukai karena kandungan seratnya yang lebih tinggi, jus sayuran mungkin masih memiliki efek positif dalam penurunan berat badan.
              Misalnya, sebuah studi kecil tahun 2010 menemukan orang dengan sindrom metabolik yang minum satu atau lebih porsi jus sayuran rendah sodium saat menjalani diet terbatas kalori kehilangan berat badan secara signifikan lebih banyak daripada mereka yang tidak melakukannya selama periode 12 minggu. Peserta juga melakukan diet yang dibatasi kalori.
            </p>
            <p>Jus sayuran tampaknya memicu penurunan leptin. Leptin adalah hormon yang dilepaskan oleh sel-sel lemak yang terkait dengan obesitas ketika kadarnya terlalu tinggi dalam darah.
            </p>
            <p><b>9. Air Kelapa</b></p>
            <p>Air kelapa adalah tambahan rendah kalori dan rendah karbohidrat yang enak untuk ditambahkan ke pilihan minuman Anda. Ini penuh dengan vitamin dan mineral seperti vitamin C dan mangan dan sangat baik untuk berolahraga. Berkat elektrolit, seperti kalium, natrium, dan magnesium, ini adalah cara yang bagus untuk membuat Anda tetap terhidrasi dan merasa segar kembali.
            </p>
            <p>Lebih khusus lagi, potasium membantu menghilangkan natrium ekstra dari tubuh Anda. Ini membantu mengurangi berat air dan dapat mendukung tujuan penurunan berat badan Anda. Pastikan Anda memilih opsi yang tidak menambahkan gula atau natrium.
            </p>
            <p><b>10. Susu Kacang</b></p>
            <p>Susu kacang termasuk varietas seperti almond dan susu kacang kedelai. Dibandingkan dengan susu sapi, susu kacang merupakan alternatif rendah kalori yang dapat membantu menurunkan berat badan.
            </p>
            <p>Susu kacang masih memiliki nutrisi penting, seperti kalsium, dan juga diperkaya dengan vitamin lain. Jenis varietas susu ini juga diperkaya dengan air, sehingga akan membantu Anda tetap terhidrasi ekstra, yang sangat bagus dalam mendukung penurunan berat badan yang sehat. Pastikan Anda menggunakan versi tanpa pemanis untuk menghindari tambahan gula dan kalori agar susu ini berfungsi sebagai minuman penurun berat badan.
            </p>
