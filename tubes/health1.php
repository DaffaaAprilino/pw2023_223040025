            <p class="card-text">Jakarta - Vitamin E merupakan salah satu suplemen penting bagi kesehatan yang berfungsi sebagai antioksidan dan pelindung sel-sel tubuh dari kerusakan. Vitamin E memiliki sifat fat soluble yang dapat larut di dalam lemak.</p>
            <p>Kandungan vitamin E dapat diperoleh dari beberapa jenis makanan seperti sayuran dan buah-buahan. Lalu, apa manfaat manfaat penting vitamin E bagi tubuh manusia?</p>
            <p>Artikel kali ini akan membahas manfaat penting kandungan vitamin E dalam makanan bagi tubuh manusia. Sekaligus beberapa contoh makanan yang mengandung vitamin E. Simak penjelasan di bawah ini, ya.</p>
            <p><b>Manfaat Penting Vitamin E</b></p>
            <p>Dikutip dari laman eprints.umm.ac.id, vitamin E merupakan vitamin yang larut dalam lemak dan sangat berguna bagi tubuh selain sebagai antioksidan juga melindungi tubuh dari polyunsaturated fatty acid (PUFAs) seperti asam oleat, asam linoleat, asam linolenat, dan asam arakidonat. Selain itu, vitamin E dipercaya juga dapat menangkal radikal bebas dan molekul oksigen dalam mencegah peroksidasi membran asam lemak tak jenuh.</p>
            <p>Untuk lebih jelasnya, berikut adalah beberapa manfaat penting vitamin E bagi kesehatan tubuh manusia:</p>
            <p><b> 1. Mencegah Radikal Bebas</b></p>
            <p>Antioksidan dalam vitamin E diyakini dapat melawan radikal bebas dalam tubuh manusia. Tubuh dapat terpapar radikal bebas yang faktor risikonya meliputi usia, pencernaan, atau faktor eksternal seperti radiasi, pencemaran lingkungan, dan asap rokok.</p>
            <p>Radikal bebas sendiri dapat menyebabkan kerusakan dalam tubuh, sehingga tubuh lebih rentan terserang berbagai penyakit. Antioksidan dalam vitamin E dapat menetralisir radikal bebas dengan cara menyumbangkan elektron yang hilang.</p>
            <p><b> 2. Menjaga Kesehatan Mata</b></p>
            <p> Dikutip dari laman WebMd, vitamin E memiliki peran penting dalam menjaga organ penglihatan manusia hingga menurunkan risiko katarak. Hal ini dapat terjadi karena vitamin E melindungi sel mata dari radikal bebas dan dapat menjaga lensa mata agar tetap bersih.</p>
            <p><b> 3. Membantu Peningkatan Kesehatan Ibu Hamil</b></p>
            <p> Vitamin E juga dipercaya sebagai nutrisi penting untuk menunjang kehamilan dan tumbuh kembang janin. Kandungan antioksidan dalam vitamin E dapat mengurangi risiko stres oksidatif selama kehamilan.</p>
            <p><b> 4. Menjaga Imunitas Tubuh</b></p>
            <p> Memiliki kandungan antioksidan, vitamin E dipercaya dapat menjaga sistem kekebalan tubuh dan pembuluh darah, serta membantu tubuh melawan bakteri dan virus. Bahkan vitamin E dapat meningkatkan antibodi dalam melawan infeksi penyakit dan radikal bebas yang mengancam daya tahan tubuh manusia.</p>
            <p><b>Contoh Makanan yang Mengandung Vitamin E</b></p>
            <p> Kandungan vitamin E bisa kamu dapatkan dalam beberapa jenis sayuran dan juga buah-buahan. Berikut adalah contoh beberapa jenis makanan yang mengandung vitamin E dikutip dari laman School of Public Health Harvard University:</p>
